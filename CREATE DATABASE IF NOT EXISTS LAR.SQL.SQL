CREATE DATABASE IF NOT EXISTS LAR;
USE LAR;

CREATE TABLE FORMULARIO_PRECISO_AJUDA(
    ID VARCHAR (255) PRIMARY KEY,
    NOME VARCHAR(255),
    TELEFONE VARCHAR(255),
    EMAIL VARCHAR(255),
    ASSUNTO VARCHAR (255),
    INSTITUICAO VARCHAR (255),
    CIDADE VARCHAR (255),
    ESTADO VARCHAR (255),
    TREMOR FLOAT,
    CANSACO FLOAT,
    DESANIMO FLOAT,
    FALTA_DE_AR FLOAT,
    AGONIA FLOAT,
    FALTA_DE_FOCO FLOAT,
    ALTERACAO_DE_HUMOR FLOAT,
    SENSACAO_DE_DESCONEXAO FLOAT,
    PREOCUPACAO_COM_PESO FLOAT,
    PERDA_DE_INTERESSE FLOAT,
    SOFRI_ABUSO_PSICOLOGICO FLOAT,
    SOFRI_ABUSO_FISICO FLOAT,
    SOFRI_ABUSO_SEXUAL FLOAT,
    SOFRI_ABUSO_PATROMONIAL FLOAT,
    SOFRI_ABUSO_MORAL FLOAT
    );

    CREATE TABLE FORMULARIO_QUERO_AJUDAR(
    ID VARCHAR (255) PRIMARY KEY,
    NOME VARCHAR(255),
    TELEFONE VARCHAR(255),
    EMAIL VARCHAR(255),
    VALOR VARCHAR(255),
    INSTITUICAO VARCHAR (255),
    CIDADE VARCHAR (255),
    ESTADO VARCHAR (255)
    );


    CREATE TABLE FALECONOSCO(
    ID VARCHAR (255) PRIMARY KEY,
    NOME VARCHAR(255),
    TELEFONE VARCHAR(255),
    EMAIL VARCHAR(255),
    ENDERECO VARCHAR(255),
    ASSUNTO VARCHAR (255),
    MENSAGEM TEXT
    );